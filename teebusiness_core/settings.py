    _h = _host.strip().rstrip('/')
    if _h and not _h.startswith('.') and _h not in ('localhost', '127.0.0.1', 'testserver'):
        _origin = f"https://{_h.split('//')[-1]}"
        if _origin not in CSRF_TRUSTED_ORIGINS:
            CSRF_TRUSTED_ORIGINS.append(_origin)

